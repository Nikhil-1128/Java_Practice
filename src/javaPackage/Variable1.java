package javaPackage;

public class Variable1 {

	public static void main(String[] args) {
		int a=5;
		System.out.println(a);
		
		int x=100;
		System.out.println(x);
		
		x=200;
		System.out.println(x);
		
		char c='N';
		System.out.println(c);
		
		boolean b=true;
		System.out.println(b);
		
		int e=100;
		int f=500;
		
		boolean d= e>f;
		System.out.println(d);
		
		System.out.println(a++);
		a=a+1;
		System.out.println(a);
		System.out.println(--a);
		

	}

}
